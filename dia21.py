# Dia 21 - Conversão de metros (km, hm, dm, cm, mm)
metro = float(input("Digite um valor em metros: "))
km = metro / 1000
print(f'km de {km} ')
hm = metro / 100
print(f'hm de {hm} ')
dec = metro / 10
print(f'dec de {dec} ')
print(f'metro de {metro} ')
dm = metro * 10
print(f'dm de {dm} ')
cm = metro * 100
print(f'cm de {cm} ')
mm = metro * 1000
print(f'mm de {mm} ')
